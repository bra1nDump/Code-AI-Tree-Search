# Solution 1

# This solution is based on the idea of dynamic programming.
# Let's consider the problem of finding the number of playlists of length $k$ that end with the $i$-th song.
# Let's denote the number of such playlists as $dp[i][k]$.
# We can calculate $dp[i][k]$ using the following formula:
# $dp[i][k] = \sum_{j=1}^{i-1} dp[j][k-1]$ if $g_i \ne g_j$
# $dp[i][k] = 0$ otherwise
# The answer is the sum of all $dp[i][k]$ for $i = 1, 2, \dots, n$ and $k = T$.

